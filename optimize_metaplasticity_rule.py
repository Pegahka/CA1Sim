"""
In this version of the simulation, phase precession of CA3 inputs is implemented using the method from Chadwick et al.,
Elife, 2015, which uses a circular gaussian with a phase sensitivity factor that effectively compresses the range of
phases within each theta cycle that each input is active, which will reduce jitter across within-cycle input sequences.

These methods determine the shape of the metaplasticity function that translates plasticity signal and initial
synaptic weight to changes in synaptic weight induced by plateaus.

Assumptions:
1) Synaptic weights are all = 1 prior to field induction 1. w(t0) = 1
2) The transfer function for field induction 1 is the identity function. w(t1) = f(w(t0), s(t0)) = s(t0) * w(t0) = s(t0)
3) The transfer function for field induction 2 is a nonlinear function of plasticity signal and initial weights.
    w(t2) = f(w(t1), s(t1))
4) The plasticity signals s(t0) and s(t1) are generated by the same local and global kernels, they are just being
    interpreted differently by signaling downstream, depending on the initial synaptic weights at each input.

"""
