                    decay_10 = np.where(interp_vm[t_peak:] <= 0.1)[0][0]
                    decay_50 = np.where(interp_vm[t_peak:] <= 0.5)[0][0]
                    #decay_tau = interp_t[decay_10] - interp_t[decay_90]
                    decay_tau = interp_t[decay_50] + interp_t[t_peak] - interp_t[rise_50]
